package org.opencms.gwt.client;

import com.google.gwt.core.client.GWT;
import com.google.gwt.resources.client.ClientBundle;
import com.google.gwt.resources.client.TextResource;

/**
 * An interface for accessing configuration resources which were included at compile-time.<p>
 * 
 */
public interface I_CmsConfigBundle extends ClientBundle {

    /** The static instance of this interface. */
    I_CmsConfigBundle INSTANCE = GWT.create(I_CmsConfigBundle.class);

    /**
     * Returns the text resource containing GWT compile time configuration entries.
     * 
     * @return the text resource for the properties file 
     */
    @Source("gwt.properties")
    TextResource gwtProperties();

}
